int euler(int n){
	int ans = n;
	for (int i = 2; i * i <= n; i++){
		if (n % i == 0){
			ans = ans / i * (i - 1); //先进行除法防止溢出(ret=ret*(1-1/p(i)))
			while (n % i == 0)
				n /= i;
		}
	}
	if (n > 1) ans = ans / n * (n - 1);
	return ans;
}
